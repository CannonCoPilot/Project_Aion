#!/usr/bin/env bash
# ollama-benchmark.sh — Ollama Model Auto-Benchmark Pipeline
#
# Polls Ollama for newly loaded models, runs a standardized benchmark suite,
# and writes routing profiles into Loom's benchmark-profiles.yaml.
#
# Designed for use as a Nexus pre_check job (zero-cost, no LLM persona needed).
# All benchmarks run via direct Ollama API calls.
#
# Usage: ollama-benchmark.sh [--force MODEL_NAME]
#   --force MODEL_NAME: force re-benchmark of a specific model

set -euo pipefail

SCRIPT_DIR="$(cd "$(dirname "${BASH_SOURCE[0]}")" && pwd)"  # exported for sourced helpers
export SCRIPT_DIR
AIPROJECTS_DIR="${PROJECT_DIR}"
LOOM_DIR="$HOME/Code/loom"
ROUTING_DATA_DIR="${LOOM_DIR}/determinism/routing_data"
BENCHMARK_PROFILES="${ROUTING_DATA_DIR}/benchmark-profiles.yaml"
KNOWN_MODELS_FILE="${AIPROJECTS_DIR}/.claude/data/ollama-benchmark-known-models.json"
RESULTS_DIR="${AIPROJECTS_DIR}/.claude/data/benchmark-results"
LOG_FILE="${AIPROJECTS_DIR}/.claude/jobs/logs/ollama-benchmark.log"
OLLAMA_URL="${OLLAMA_URL:-http://localhost:11434}"

FORCE_MODEL=""
if [[ "${1:-}" == "--force" && -n "${2:-}" ]]; then
  FORCE_MODEL="$2"
fi

# Per-test curl timeout — override via env: CURL_TIMEOUT=90 ./ollama-benchmark.sh --force <model>
CURL_TIMEOUT="${CURL_TIMEOUT:-45}"

mkdir -p "$(dirname "$LOG_FILE")"
log() { echo "[$(date -u '+%Y-%m-%dT%H:%M:%SZ')] $*" | tee -a "$LOG_FILE"; }

# Step 1: Get current models from Ollama
log "Polling Ollama for available models..."
if ! OLLAMA_MODELS_JSON=$(curl -sf "${OLLAMA_URL}/api/tags" 2>/dev/null); then
  log "ERROR: Cannot reach Ollama at ${OLLAMA_URL}"
  exit 1
fi

CURRENT_MODELS=$(echo "$OLLAMA_MODELS_JSON" | python3 -c "
import json, sys
data = json.load(sys.stdin)
for m in data.get('models', []):
    name = m.get('name','')
    size = m.get('size', 0)
    if 'embed' in name.lower():
        continue
    print(f'{name}\t{size}')
" 2>/dev/null || echo "")

if [[ -z "$CURRENT_MODELS" ]]; then
  log "No generative models found in Ollama."
  exit 1
fi

# Step 2: Load known/registered models
if [[ ! -f "$KNOWN_MODELS_FILE" ]]; then
  echo '{}' > "$KNOWN_MODELS_FILE"
fi
KNOWN_MODELS=$(cat "$KNOWN_MODELS_FILE")

# Step 3: Identify new models
NEW_MODELS=()
while IFS=$'\t' read -r model_name _model_size; do
  [[ -z "$model_name" ]] && continue
  IS_KNOWN=$(echo "$KNOWN_MODELS" | python3 -c "
import json, sys
data = json.load(sys.stdin)
print('yes' if '$model_name' in data else 'no')
" 2>/dev/null || echo "no")

  if [[ "$IS_KNOWN" == "no" ]] || [[ "$FORCE_MODEL" == "$model_name" ]]; then
    NEW_MODELS+=("$model_name")
  fi
done <<< "$CURRENT_MODELS"

if [[ ${#NEW_MODELS[@]} -eq 0 ]]; then
  log "No new models to benchmark. All models registered."
  exit 0
fi

log "Found ${#NEW_MODELS[@]} new model(s) to benchmark: ${NEW_MODELS[*]}"

# Step 4: Benchmark each new model
mkdir -p "$RESULTS_DIR"

# Initialize benchmark-profiles.yaml if missing
if [[ ! -f "$BENCHMARK_PROFILES" ]]; then
  cat > "$BENCHMARK_PROFILES" << 'YAML'
# Loom Benchmark Profiles — Auto-generated by ollama-benchmark.sh
# Format: per-model capability and performance scores
# These profiles are used as routing hints when Loom benchmark suite formalizes.
version: 1
note: "Stub benchmarks — prompts will be upgraded when Loom test suite formalizes"
profiles: {}
YAML
  log "Created new benchmark-profiles.yaml"
fi

run_benchmark() {
  local model="$1"
  local prompt="$2"
  local expected_pattern="$3"
  local timeout_sec="${4:-30}"

  local start_ms
  start_ms=$(date +%s%3N)

  local response
  response=$(curl -sf --max-time "$timeout_sec" \
    -X POST "${OLLAMA_URL}/api/generate" \
    -H "Content-Type: application/json" \
    -d "{\"model\":\"${model}\",\"prompt\":$(echo "$prompt" | python3 -c "import json,sys; print(json.dumps(sys.stdin.read()))"),\"stream\":false,\"options\":{\"num_predict\":200}}" \
    2>/dev/null || echo '{"error":"timeout"}')

  local end_ms
  end_ms=$(date +%s%3N)
  local elapsed_ms=$(( end_ms - start_ms ))

  local resp_text eval_count
  resp_text=$(echo "$response" | python3 -c "import json,sys; d=json.load(sys.stdin); print(d.get('response',''))" 2>/dev/null || echo "")
  eval_count=$(echo "$response" | python3 -c "import json,sys; d=json.load(sys.stdin); print(d.get('eval_count',0))" 2>/dev/null || echo "0")

  local score=0
  if [[ -n "$expected_pattern" && -n "$resp_text" ]]; then
    if echo "$resp_text" | grep -qi "$expected_pattern"; then
      score=1
    fi
  elif [[ -n "$resp_text" ]]; then
    score=1
  fi

  local tps=0
  if [[ "$elapsed_ms" -gt 0 && "$eval_count" -gt 0 ]]; then
    tps=$(python3 -c "print(round(${eval_count} * 1000 / ${elapsed_ms}, 1))" 2>/dev/null || echo "0")
  fi

  echo "${score}|${elapsed_ms}|${tps}|${eval_count}"
}

for MODEL in "${NEW_MODELS[@]}"; do
  log "Benchmarking: $MODEL"
  BENCH_START=$(date +%s%3N)

  log "  [1/5] Reasoning..."
  R1=$(run_benchmark "$MODEL" "What is the next number in the Fibonacci sequence after 13? Answer with just the number." "21" "$CURL_TIMEOUT")
  R1_SCORE=$(echo "$R1" | cut -d'|' -f1); R1_MS=$(echo "$R1" | cut -d'|' -f2); R1_TPS=$(echo "$R1" | cut -d'|' -f3)

  log "  [2/5] Coding..."
  R2=$(run_benchmark "$MODEL" "Write a Python function called is_prime(n) that returns True if n is prime. Return only the code." "def is_prime" "$CURL_TIMEOUT")
  R2_SCORE=$(echo "$R2" | cut -d'|' -f1); R2_MS=$(echo "$R2" | cut -d'|' -f2); R2_TPS=$(echo "$R2" | cut -d'|' -f3)

  log "  [3/5] Instruction following..."
  R3=$(run_benchmark "$MODEL" 'Respond with ONLY valid JSON, no explanation: {"name": "Alice", "age": 30, "city": "Denver"}' '"name"' "$CURL_TIMEOUT")
  R3_SCORE=$(echo "$R3" | cut -d'|' -f1); R3_MS=$(echo "$R3" | cut -d'|' -f2); R3_TPS=$(echo "$R3" | cut -d'|' -f3)

  log "  [4/5] Context handling..."
  R4=$(run_benchmark "$MODEL" "Read this: The capital of France is Paris. The capital of Japan is Tokyo. The capital of Brazil is Brasilia. Answer only: What is the capital of Japan?" "Tokyo" "$CURL_TIMEOUT")
  R4_SCORE=$(echo "$R4" | cut -d'|' -f1); R4_MS=$(echo "$R4" | cut -d'|' -f2); R4_TPS=$(echo "$R4" | cut -d'|' -f3)

  log "  [5/5] Speed test..."
  R5=$(run_benchmark "$MODEL" "Write a short story opening paragraph about a robot who discovers music." "" "$(( CURL_TIMEOUT > 60 ? CURL_TIMEOUT : 60 ))")
  R5_MS=$(echo "$R5" | cut -d'|' -f2); R5_TPS=$(echo "$R5" | cut -d'|' -f3); R5_TOKENS=$(echo "$R5" | cut -d'|' -f4)

  BENCH_END=$(date +%s%3N)
  BENCH_TOTAL_MS=$(( BENCH_END - BENCH_START ))

  TOTAL_SCORED=$(( R1_SCORE + R2_SCORE + R3_SCORE + R4_SCORE ))
  ACCURACY_PCT=$(python3 -c "print(round(${TOTAL_SCORED} / 4 * 100))" 2>/dev/null || echo "0")
  AVG_TPS=$(python3 -c "
vals = [x for x in [${R1_TPS}, ${R2_TPS}, ${R3_TPS}, ${R4_TPS}, ${R5_TPS}] if x > 0]
print(round(sum(vals)/len(vals), 1) if vals else 0)
" 2>/dev/null || echo "0")

  RECOMMENDED=""
  [[ "$R1_SCORE" -eq 1 ]] && RECOMMENDED="${RECOMMENDED}reasoning,"
  [[ "$R2_SCORE" -eq 1 ]] && RECOMMENDED="${RECOMMENDED}coding,"
  [[ "$R3_SCORE" -eq 1 ]] && RECOMMENDED="${RECOMMENDED}structured-output,"
  [[ "$R4_SCORE" -eq 1 ]] && RECOMMENDED="${RECOMMENDED}context-retrieval,"
  RECOMMENDED="${RECOMMENDED}generation"

  MODEL_SIZE=$(echo "$OLLAMA_MODELS_JSON" | python3 -c "
import json, sys
data = json.load(sys.stdin)
for m in data.get('models', []):
    if m.get('name') == '${MODEL}':
        print(f\"{round(m.get('size',0)/1024**3,1)}GB\")
        break
else:
    print('unknown')
" 2>/dev/null || echo "unknown")

  YAML_KEY=$(echo "$MODEL" | tr ':/' '__')
  BENCH_DATE=$(date -u '+%Y-%m-%dT%H:%M:%SZ')
  BENCH_DATE_SHORT=$(date -u '+%Y-%m-%d')

  # Write raw results JSONL
  RESULTS_FILE="${RESULTS_DIR}/${BENCH_DATE_SHORT}-benchmark-results.jsonl"
  mkdir -p "$RESULTS_DIR"
  python3 -c "
import json
record = {
    'model': '${MODEL}',
    'benchmarked_at': '${BENCH_DATE}',
    'size': '${MODEL_SIZE}',
    'tests': {
        'reasoning': {'score': ${R1_SCORE}, 'latency_ms': ${R1_MS}, 'tps': ${R1_TPS}},
        'coding': {'score': ${R2_SCORE}, 'latency_ms': ${R2_MS}, 'tps': ${R2_TPS}},
        'instruction_following': {'score': ${R3_SCORE}, 'latency_ms': ${R3_MS}, 'tps': ${R3_TPS}},
        'context_handling': {'score': ${R4_SCORE}, 'latency_ms': ${R4_MS}, 'tps': ${R4_TPS}},
        'speed': {'latency_ms': ${R5_MS}, 'tps': ${R5_TPS}, 'tokens_generated': ${R5_TOKENS}}
    },
    'summary': {'accuracy_pct': ${ACCURACY_PCT}, 'avg_tps': ${AVG_TPS}, 'total_bench_ms': ${BENCH_TOTAL_MS}}
}
print(json.dumps(record))
" >> "$RESULTS_FILE" 2>/dev/null
  log "  Raw results written: accuracy=${ACCURACY_PCT}%, avg_tps=${AVG_TPS}"

  # Update benchmark-profiles.yaml (idempotent)
  python3 << PYEOF
import re
from pathlib import Path

p = Path("${BENCHMARK_PROFILES}")
content = p.read_text()
key = "${YAML_KEY}"
block = f"""  {key}:
    model_id: "${MODEL}"
    size: "${MODEL_SIZE}"
    benchmarked_at: "${BENCH_DATE}"
    benchmark_version: stub-v1
    note: Stub benchmarks — upgrade when Loom test suite formalizes
    scores:
      reasoning: ${R1_SCORE}
      coding: ${R2_SCORE}
      instruction_following: ${R3_SCORE}
      context_handling: ${R4_SCORE}
      accuracy_pct: ${ACCURACY_PCT}
    performance:
      avg_tps: ${AVG_TPS}
      speed_tps: ${R5_TPS}
      total_bench_ms: ${BENCH_TOTAL_MS}
    recommended_task_types: [${RECOMMENDED}]
    review_status: pending
    activated_in_routing: false
"""
# Remove existing entry (idempotent update)
pattern = rf"  {re.escape(key)}:.*?(?=\n  [a-zA-Z_]|\Z)"
content = re.sub(pattern, "", content, flags=re.DOTALL)

if "profiles: {}" in content:
    content = content.replace("profiles: {}", f"profiles:\n{block}")
elif "profiles:\n" in content:
    content = content.replace("profiles:\n", f"profiles:\n{block}")
else:
    content += f"\n{block}"
p.write_text(content)
PYEOF

  # Register in known-models
  python3 -c "
import json
from pathlib import Path
p = Path('${KNOWN_MODELS_FILE}')
data = json.loads(p.read_text())
data['${MODEL}'] = {'registered_at': '${BENCH_DATE}', 'size': '${MODEL_SIZE}', 'accuracy_pct': ${ACCURACY_PCT}, 'avg_tps': ${AVG_TPS}}
p.write_text(json.dumps(data, indent=2))
"
  log "  Registered ${MODEL} in known-models registry."
done

log "Benchmark pipeline complete. Profiles: ${BENCHMARK_PROFILES}"
exit 0
